class EscapeCharacter {
	public static void main( String[] args ) {

		System.out.print("\n  here i used next line Escape character ");
		System.out.print("\b  here i used Backspace Escape character ");
		System.out.print("\t  here i used tab Escape character ");
		System.out.print("\r  here i used carrige return Escape character ");
		System.out.print("\f  here i used back slash f it will insert form feed in the text at this point ");
		System.out.print("\'  here i used single quote Escape character ");
		System.out.print("\"  here i used double quote Escape character ");
		System.out.print("\\  here i used backslash character ");
	
	}
}